def congestion_level_from_count(count, thresholds=CONGESTION_THRESHOLDS):
    if count >= thresholds["High"]:
        return "High"
    elif count >= thresholds["Medium"]:
        return "Medium"
    else:
        return "Low"

def mqtt_publish(client, topic, payload):
    client.publish(topic, json.dumps(payload))

