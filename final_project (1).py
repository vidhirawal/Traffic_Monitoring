# MQTT
mqtt_client = mqtt.Client()
try:
    mqtt_client.connect(MQTT_BROKER, MQTT_PORT, 60)
    mqtt_client.loop_start()
    print("📡 MQTT connected.")
except Exception as e:
    print("⚠️ MQTT skipped:", e)
    mqtt_client = None

telemetry = []

max_frames = 300  # for quick demo; None = all
frame_idx = 0

pbar = tqdm(total=min(frames_total, max_frames if max_frames else frames_total))
while True:
    ret, frame = cap.read()
    if not ret:
      break
    frame_idx += 1
    if max_frames and frame_idx > max_frames:
        break

    results = model.predict(source=frame, imgsz=640, conf=0.35, verbose=False)
    boxes = results[0].boxes
    count = 0
    for box in boxes:
        cls = int(box.cls[0])
        if cls in VEHICLE_CLASS_IDS:
            count += 1

    congestion = congestion_level_from_count(count)
    annotated = results[0].plot()
    cv2.putText(annotated, f"Frame:{frame_idx}  Vehicles:{count}  Cong:{congestion}",
                (20,40), cv2.FONT_HERSHEY_SIMPLEX, 1, (0,255,0), 2)
    out.write(annotated)

    data = {"timestamp": datetime.utcnow().isoformat()+"Z",
            "frame": frame_idx, "vehicles": count, "congestion": congestion}
    telemetry.append(data)
    if mqtt_client:
        mqtt_publish(mqtt_client, MQTT_TOPIC, data)
    pbar.update(1)

pbar.close()
cap.release()
out.release()
if mqtt_client:
    mqtt_client.loop_stop()
    mqtt_client.disconnect()

