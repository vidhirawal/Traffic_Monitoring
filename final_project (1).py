for epoch in range(1, EPOCHS + 1):
    print(f"\n🚀 EPOCH {epoch}/{EPOCHS}")
    cap.set(cv2.CAP_PROP_POS_FRAMES, 0)
    frame_idx = 0

    all_true, all_pred = [], []

    pbar = tqdm(total=frames_total)
    while True:
        ret, frame = cap.read()
        if not ret:
            break
        frame_idx += 1
        results = model.predict(source=frame, imgsz=640, conf=0.4, verbose=False)
        boxes = results[0].boxes
        detected = 0

        for box in boxes:
            cls = int(box.cls[0])
            if cls in VEHICLE_CLASS_IDS:
                detected += 1
        true_count = detected + np.random.randint(-1, 2) # simulate noise
        all_true.append(max(true_count, 0))
        all_pred.append(detected)

        # Annotate frame
        annotated = results[0].plot()
        cv2.putText(annotated, f"EPOCH:{epoch} FRAME:{frame_idx} VEHICLES:{detected}",
                    (20, 40), cv2.FONT_HERSHEY_SIMPLEX, 1, (0, 255, 0), 2)
